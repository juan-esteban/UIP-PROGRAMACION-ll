public class Policia {
    private String nombre;
    private String rango;

    public void setNombre(String n) {
        this.nombre = n;
    }

    public String getNombre() {
        return this.nombre;
    }

    public void setRango(String r) {
        this.rango = r;
    }

    public String getRango() {
        return this.rango;
    }
}
